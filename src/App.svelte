    <h1>History</h1>
    <p>
        National Higher Secondary School, situated in the heart of Irinjalakuda,
        a city famous as the birth place of World Famous
        Mathematician-Astronomer Sangamagrama Madhavan (who founded the Kerala
        school of astronomy and mathematics), the famous carnatic singer and
        actress Nagapattinam Chandrashekhara Vasanthakokilam), the famous author
        of Nalacharitham aattakatha Unnayi Warrier, who is known to have made
        significant contributions to the art of Kathakali, the classical
        dance-drama of Kerala, Koodiyattom Mastero Ammannur Madhava Chakyar
        etc...<br /><br />

        The school was founded in the year 1935 and is now completing 87 years
        of dedicated service. The school celebrated its Platinum Jubilee in
        2011. In 1942, the first batch of students appeared for the 7 th
        standard public examination. From 1944 to 1991, Sri V K Menon was the
        respected Manager of this school. With his sincere and relentless
        efforts, the school achieved pre eminence in all fields. In 1991-1992,
        Sri V K Menon handed over Management of the school to his daughter
        Smt.Rukmani Ramachandran due to his failing health. He passed away in
        2007.<br /><br />

        Since taking over the management of school, Smt.Rukmani Ramachandran and
        her husband and Management Representative Sri.V P R Menon are constant
        sources of inspiration, guidance and leadership. Now the school is
        steadily progressing under the strong leadership of Smt. Rukmani
        Ramachandran and Sri. V P R Menon.<br /><br />

        The Higher Secondary Section of th is school started in 1998. From 2005
        to 2019, Smt Mini Mohandas was the Principal of this school. There are
        LP, UP, High School and Higher Secondary Sections in this school. In
        addition to academic activities, the school is one of the bestschools
        for extra curricular activities. In the High school section, there are
        NCC,SPC and JRC wings and in the higher secondary section, a strong NSS
        team, Souhrida Club, Career Guidance and Seed Wing are all very much
        active. At present, Smt Lisha V V is the principal and Smt. Sudha K K is
        the Headmistress and both are providing able leadership for maintaining
        the high standards the school has set for itself.
    </p>
